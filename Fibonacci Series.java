import java.util.Scanner;

public class Fibonacci {
  public static void main (String[] args) {
    Scanner in = new Scanner (System.in);
    System.out.print("Enter the number: ");
    int n = in.nextInt();
    int sum=0, a=1, b=1;
    System.out.print("1 1");
    while (sum<=n) {
      sum=a+b;
      if (sum>n)
      break;
      System.out.print(sum+" ");
      a=b;
      b=sum;
    }
  }
}
